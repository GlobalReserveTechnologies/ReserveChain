      <article class="faq-item">
        <h4>Is this just another Time Wonderland clone?</h4>
        <p>
          No. ReserveChain fixes the core problems that broke earlier experiments: reflexive APY,
          opaque reserves, and no coherent corridor model. Issuance is deterministic, coverage is
          tracked via vaults, and corridor bands are explicit.
        </p>
      </article>

      <article class="faq-item">
        <h4>How is privacy handled without hiding reserves?</h4>
        <p>
          Reserves, coverage, and policy are public. Individual balances and flows can be shielded
          via vaults and stealth addresses. Vault-level aggregates can still be audited and stress
          tested without doxxing individual users.
        </p>
      </article>

      <article class="faq-item">
        <h4>Do I connect a browser wallet like MetaMask?</h4>
        <p>
          The devnet focuses on the protocol and workstation first. The long-term goal is a
          workstation-style experience with safer signing than browser plug-ins, aligned with
          private banking practices rather than retail DeFi.
        </p>
      </article>

      <article class="faq-item">
        <h4>What runs on my own node vs. the main UI server?</h4>
        <p>
          The main UI server hosts the marketing site and workstation shell. Node clusters sit
          behind gateways, handling blocks, vaults, and settlement. Operators can still run local
          workstations against their own nodes for governance and analytics.
        </p>
      </article>
