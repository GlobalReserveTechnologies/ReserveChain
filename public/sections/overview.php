    <header class="section-header">
      <h2 class="section-kicker">Why ReserveChain</h2>
      <h3 class="section-title">
        One reserve network, three surfaces:
        <span>protocol, vault, and finance workstation.</span>
      </h3>
      <p class="section-lede">
        ReserveChain is a hybrid layer-1 designed for private wealth protection and deterministic
        reserve management. The chain, vault, and workstation all speak the same economic language:
        corridor bands, utilization-aware issuance, and verifiable coverage.
      </p>
    </header>

    <div class="overview-grid">
      <article class="overview-card">
        <h4>Protocol Layer</h4>
        <p>
          A PoW → PoS → PoP consensus engine with deterministic issuance and corridor-based
          stabilization. Blocks carry transactions, reserve adjustments, and governance decisions
          in a single, auditable ledger.
        </p>
        <ul class="overview-list">
          <li>Hybrid PoW / PoS / Proof-of-Participation</li>
          <li>Deterministic issuance schedule with epochs</li>
          <li>Corridor bands instead of “infinite APY” gimmicks</li>
        </ul>
      </article>

      <article class="overview-card">
        <h4>Vault &amp; Privacy Layer</h4>
        <p>
          Vaults act like a private bank on-chain: assets can be deposited into opaque vaults,
          routed via stealth addresses, and redeemed through corridor-aware settlement tickets.
        </p>
        <ul class="overview-list">
          <li>Multi-asset reserve vaults with coverage tracking</li>
          <li>Stealth addresses for private inbound and outbound flows</li>
          <li>Vault-scoped analytics for regulators and auditors</li>
        </ul>
      </article>

      <article class="overview-card">
        <h4>Workstation</h4>
        <p>
          A professional finance workstation sitting on top of the protocol. Trading terminal,
          lending, analytics, governance, and explorer share a single reserve engine and risk model.
        </p>
        <ul class="overview-list">
          <li>Day-trader terminal with on-chart order controls</li>
          <li>Reserve-aware lending &amp; synthetic positions</li>
          <li>Node operator views, governance, and risk dashboards</li>
        </ul>
      </article>
    </div>
