    <header class="section-header">
      <h2 class="section-kicker">Privacy &amp; Stealth</h2>
      <h3 class="section-title">
        Public reserves, <span>private balances.</span>
      </h3>
      <p class="section-lede">
        ReserveChain separates what needs to be public from what deserves to be private. Reserves,
        coverage, and policy are fully visible. Individual balances and movements can be shielded
        via vaults and stealth addresses.
      </p>
    </header>

    <div class="privacy-grid">
      <article class="privacy-card">
        <h4>Stealth Addresses</h4>
      <article class="privacy-card">
        <h4>Private Vaults</h4>
        <p>
          Vaults behave like private banking sub-accounts on top of the ReserveChain ledger. Each
          vault can have its own label, visibility policy, and yield profile while still sharing
          the same global reserve backing.
        </p>
        <ul>
          <li>Per-vault visibility modes for reports and exports</li>
          <li>Isolated balances for client portfolios or strategies</li>
          <li>Optional P&amp;L settlement modes configured from the workstation</li>
        </ul>
      </article>

        <p>
          Users can generate stealth addresses for private deposits and withdrawals. Observers see
          value flowing in and out of vaults, but cannot trivially link flows back to individuals.
        </p>
        <ul>
          <li>Per-user stealth address trees</li>
          <li>One-time receive addresses for inbound flows</li>
          <li>Configurable disclosure for audits and compliance</li>
        </ul>
      </article>

      <article class="privacy-card">
        <h4>Vault-Layer Privacy</h4>
        <p>
          Vaults publish aggregate coverage and composition, not per-account holdings. This mirrors
          how a private bank publishes balance sheet strength without doxing its clients.
        </p>
        <ul>
          <li>Public vault coverage and composition metrics</li>
          <li>Private account-level balance and transaction history</li>
          <li>Optional “proof of solvency” channels for regulators</li>
        </ul>
      </article>

      <article class="privacy-card">
        <h4>Workstation Controls</h4>
        <p>
          Inside the workstation, privacy is not an afterthought. Users can choose when to route via
          stealth, when to expose flows, and how to structure vault usage over time.
        </p>
        <ul>
          <li>Per-transfer privacy mode (direct vs stealth)</li>
          <li>Visibility controls for exports and reports</li>
          <li>Clear warnings when revealing additional information</li>
        </ul>
      </article>
    </div>
  </div>
</section>
